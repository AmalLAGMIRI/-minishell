#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*r;
	int		i;

	i = 0;
	len = ft_strlen(s1);
	r = (char *)malloc (len * sizeof (char) + 1);
	if (r == NULL)
		return (0);
	while (s1[i])
	{
		r[i] = s1[i];
		i++;
	}
	r[i] = '\0';
	return (r);
}
